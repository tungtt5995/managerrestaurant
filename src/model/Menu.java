package model;

/**
 *
 * @author Admin
 */
public class Menu {
    private int maMon;
    private String tenMon;
    private int maLoai;
    private double donGia;
    private String DVT;

    public int getMaMon() {
        return maMon;
    }

    public void setMaMon(int maMon) {
        this.maMon = maMon;
    }

    public String getTenMon() {
        return tenMon;
    }

    public void setTenMon(String tenMon) {
        this.tenMon = tenMon;
    }

    public int getMaLoai() {
        return maLoai;
    }

    public void setMaLoai(int maLoai) {
        this.maLoai = maLoai;
    }

    public double getDonGia() {
        return donGia;
    }

    public void setDonGia(double donGia) {
        this.donGia = donGia;
    }

    public String getDVT() {
        return DVT;
    }

    public void setDVT(String DVT) {
        this.DVT = DVT;
    }

    @Override
    public String toString() {
        return "Menu{" + "maMon=" + maMon + ", tenMon=" + tenMon + ", maLoai=" + maLoai + ", donGia=" + donGia + ", DVT=" + DVT + '}';
    }
    
    
}
